class FactModule {
  constructor(client, bot) {
    this.client = client;
    this.bot = bot;
    this.name = 'fact';
    this.facts = [
      '🧠 Мозг человека потребляет 20% всей энергии тела, хотя весит всего 2% от массы тела.',
      '🐢 Черепахи могут дышать через задницу. Это называется клоакальное дыхание.',
      '🌙 Луна от Земли удаляется со скоростью примерно 3.8 см в год.',
      '🍯 Пчелы могут распознавать человеческие лица.',
      '💎 Алмазы могут гореть. Температура горения алмаза — около 800°C.',
      '🦈 Акулы старше деревьев. Они появились 400 миллионов лет назад, а деревья — 350 миллионов.',
      '🧬 ДНК человека на 60% совпадает с ДНК банана.',
      '🐙 Осьминог имеет три сердца и голубую кровь.',
      '🦅 Орёл может увидеть мышь с расстояния 2 километра.',
      '🐘 Слоны — единственные животные, которые не умеют прыгать.',
      '🦋 Бабочки пробуют пищу ногами.',
      '🕷 Паутина прочнее стали того же диаметра.',
      '🚗 Первый водитель, оштрафованный за превышение скорости, ехал со скоростью 8 км/ч.',
      '🐧 Пингвины делают предложение своим самкам, давая им камень.',
      '👻 В Исландии больше книг, чем в любой другой стране.'
    ];
  }

  getCommands() {
    return ['факт', 'интересный факт', 'fact'];
  }

  async handleMessage(msg, text) {
    const cmd = text.toLowerCase().trim();
    if (cmd === 'факт' || cmd === 'интересный факт' || cmd === 'fact') {
      await this.sendFact(msg);
      return true;
    }
    return false;
  }

  async sendFact(msg) {
    const fact = this.facts[Math.floor(Math.random() * this.facts.length)];
    await this.client.sendMessage(msg.chatId, { message: `📚 **ИНТЕРЕСНЫЙ ФАКТ**\n\n${fact}`, parseMode: 'markdown', replyTo: msg.id });
  }
}

module.exports = FactModule;